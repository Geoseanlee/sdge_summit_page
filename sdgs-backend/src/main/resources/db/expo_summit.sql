  `id` BIGINT NOT NULL AUTO_INCREMENT COMMENT '主键ID',
  `data_type` VARCHAR(20) NOT NULL COMMENT '数据类型：schedule-议程，guest-嘉宾',
  `date` VARCHAR(20) DEFAULT NULL COMMENT '日期，如8月24日（仅议程类型使用）',
  `time` VARCHAR(50) DEFAULT NULL COMMENT '时间，如17:00前（仅议程类型使用）',
